/**
 * MAIN EXPORT
 * Returns a map of filenames to content
 */
export const generateFlutterProject = (config, supabaseUrl, supabaseKey) => {
  const files = {};

  // Root
  files['pubspec.yaml'] = generatePubspec(config);
  files['README.md'] = `# ${config.name}\nGenerated by EventForge.`;
  files['.github/workflows/build.yml'] = generateWorkflow(config);
  
  // Android Manifest Overlay (to be copied by workflow)
  files['android_xml_overlay/AndroidManifest.xml'] = generateAndroidManifest(config);

  // Lib
  files['lib/main.dart'] = generateMain(config, supabaseUrl, supabaseKey);
// ... existing code ...
  files['lib/theme.dart'] = generateTheme(config);
  
  // Admin Screens
  if (config.adminPassword) {
      files['lib/screens/admin_login_screen.dart'] = generateAdminLogin(config);
      files['lib/screens/admin_dashboard_screen.dart'] = generateAdminDashboard(config);
  }

  // Screens
  config.screens.forEach(screen => {
      const filename = `lib/screens/${screen.id}_screen.dart`;
      files[filename] = generateScreen(screen, config);
  });

  return files;
};
