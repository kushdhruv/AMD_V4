const generateMain = (config, supabaseUrl, supabaseKey) => {
  const firstScreen = config.screens[0];
  const screenId = firstScreen.id;
  
  // Generate imports for all screens
  const imports = config.screens.map(s => 
      `import 'screens/${s.id}_screen.dart';` 
  ).join('\n');

  // Generate routes map
  let routes = config.screens.map(s => {
      const cls = s.id.charAt(0).toUpperCase() + s.id.slice(1) + 'Screen';
      return `'/${s.id}': (context) => const ${cls}(),`;
  }).join('\n        ');

  // Admin Routes
  if (config.adminPassword) {
      routes += `\n        '/admin_login': (context) => const AdminLoginScreen(),`;
      routes += `\n        '/admin_dashboard': (context) => const AdminDashboardScreen(),`;
  }

  return `import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';
import 'package:supabase_flutter/supabase_flutter.dart';
import 'theme.dart';
${imports}
${config.adminPassword ? "import 'screens/admin_login_screen.dart';" : ""}
${config.adminPassword ? "import 'screens/admin_dashboard_screen.dart';" : ""}

Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized();
  
  await Supabase.initialize(
    url: '${supabaseUrl}',
    anonKey: '${supabaseKey}',
  );

  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: '${config.name}',
      debugShowCheckedModeBanner: false,
      theme: AppTheme.lightTheme,
      initialRoute: '/${screenId}',
      routes: {
        ${routes}
      },
    );
  }
}
`;
};

